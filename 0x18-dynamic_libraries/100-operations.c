#include <stdio.h>

/**
 * sum - add numbers
 * @a: integer 1
 * @b: integer 2
 *
 * Return: sum
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - functions to  subtract numbers
 * @a: integer 1
 * @b: integer 2
 *
 * Return: difference
 */
int sub(int a, int b)
{
        return (a - b);
}
/**
 * div - functions to divide numbers
 * @a: integer 1
 * @b: integer 2
 *
 * Return: the divident
 */
int div(int a, int b)
{
        return (a / b);
}
/**
 * mul - functions to multiplies numbers
 * @a: integer 1
 * @b: integer 2
 *
 * Return: the product
 */
int mul(int a, int b)
{
        return (a * b);
}
/**
 * mod - returns the remainder of a number
 * @a: integer 1
 * @b: integer 2
 *
 * Return: remainder
 */
int mod(int a, int b)
{
        return (a % b);
}
